    public static void listar(DefaultTableModel modelo) {

        Collections.sort(usuarios, (Usuario o1, Usuario o2) -> {
            return new Integer(o2.getTiempo()).compareTo(new Integer(o1.getTiempo()));
        });
        
        if (!usuarios.isEmpty()) {
            txtMejorPuntaje.setText(usuarios.get(0).getTiempo() + "");

        }
        modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Tiempo(segundos)");
        for (int i = 0; i < usuarios.size(); i++) {
            modelo.addRow(new Object[]{
                usuarios.get(i).getNombreUsuario(),
                usuarios.get(i).getTiempo()
            });
        }

        tblTabla.setModel(modelo);
    }

