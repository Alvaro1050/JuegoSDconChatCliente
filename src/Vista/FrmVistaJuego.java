    private void btnIniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniActionPerformed
        // TODO add your handling code here:

        crono = new Cronometro("Cronometro");
        crono.start();
        hilos = new ArrayList<>(60);
        dws = new ArrayList<>(60);

        btnIni.setEnabled(false);
        btnReini.setEnabled(true);
        dw2 = new DrawableCanvasB(JPanelJog);
        dw2.banderaFinalizado = false;
        for (int i = 0; i < 30; i++) {
            dws.add(new DrawableCanvas(JPanelJog));
            hilos.add(new Thread(dws.get(i), "Bolas"));
        }

        for (int i = 0; i < 30; i++) {
            hilos.get(i).start();
        }
        bolas = dws.size();

        hilo3 = new Thread(dw2, "Proceso Grafico");
        hilo3.start();
    }//GEN-LAST:event_btnIniActionPerformed

