package Modelo;

/**
 *
 * @author alvar
 */
public class Ball {

    private int ancho;
    private int alto;
    private int posX;
    private int posY;

    public Ball(int ancho, int alto, int postX, int postY) {
        this.ancho = ancho;
        this.alto = alto;
        this.posX = postX;
        this.posY = postY;
    }

    public int getAncho() {
        return ancho;
    }

    public void setAncho(int ancho) {
        this.ancho = ancho;
    }

    public int getAlto() {
        return alto;
    }

    public void setAlto(int alto) {
        this.alto = alto;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }

}
